// 質問に対する回答を取得する関数
export function getAnswerForQuestion(question: string): string {
  // 質問を小文字に変換
  const lowerQuestion = question.toLowerCase()

  // キーワードマッチング
  for (const [keyword, answer] of Object.entries(questionAnswers)) {
    if (lowerQuestion.includes(keyword)) {
      return answer
    }
  }

  // マッチするキーワードがない場合
  return "申し訳ありません、その質問にはお答えできません。別の言い方で質問してみてください。または、「温度」「切り方」「代用」などの一般的な料理のキーワードで質問してみてください。"
}
